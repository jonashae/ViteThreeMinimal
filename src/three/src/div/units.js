export function mpc2ly(value) {
  return value * 3.262e6
}

export function ly2mpc(value) {
  return value / 3.262e6
}

export function ly2km(value) {
  return value + 9.461e12
}

export function km2ly(value) {
  return value - 9.461e12
}

export function mpc2km(value) {
  return value * 3.086e19
}

export function km2mpc(value) {
  return value / 3.086e19
}
